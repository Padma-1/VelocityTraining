package basics;

public class _17ForDemo {

	public static void main(String[] args) {
		int i,j;
		System.out.println("*********Display 1-25***********");
		for(i=1;i<=25;i++) {
			System.out.println(i);
		}
		System.out.println("******Display 25-1*********");
		for(j=25;j>=1;j--) {
			System.out.println(j);
		}

	}

}
